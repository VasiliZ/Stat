public class Rai {
    String soato;
    String name;

    public Rai() {
    }

    public Rai(String soato, String name) {
        this.soato = soato;
        this.name = name;
    }

    public String getSoato() {
        return soato;
    }

    public void setSoato(String soato) {
        this.soato = soato;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
